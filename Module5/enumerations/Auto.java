package Module5.enumerations;

public class Auto {
    public static void main (String args[]){

        // auto-boxing
        int num = 10;
        Integer num1 = num;

        System.out.println(num1);

        // auto-unboxing
        Integer a = 8;
        int b = a;

        System.out.println(b);
        

    } 
}
